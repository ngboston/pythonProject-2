#  Користувач вводить з клавіатури два числа (початок та
# кінець діапазону). Проаналізуйте усі числа у цьому діапазоні за
# наступним правилом: якщо число кратне 7, його треба вивести
# на екран

a=int(input('Enter the start of the range : '))
b=int(input('Enter the end of the range : '))
for i in range(a,b+1):
    if i % 7 == 0:
        print(i, end=' ')
